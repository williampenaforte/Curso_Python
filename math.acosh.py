#math.acosh()
#Retorne o cosseno hiperbólico inverso de diferentes números:

import math

print (math.acosh(7))
print (math.acosh(56))
print (math.acosh(2.45))
print (math.acosh(1))

#Definição e Uso
#O math.acosh()método retorna o cosseno hiperbólico inverso de um número.

#Obs: O parâmetro passado em acosh() deve ser maior ou igual a 1.

